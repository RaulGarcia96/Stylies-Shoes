    private void btnSumaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSumaActionPerformed
        // TODO add your handling code here:
        double subtotal = 0.0;
        double ivaSub = 0.0;
        double total = 0.0;
        final double IVA = 0.16;

        if (cbxZapatilla.isSelected()) {
            subtotal += 500;
            nombreTamano = nombreTamano + "  Zapatillas de ahuja Magnetica color: Dorado  ";
        }
        if (cbxTenis.isSelected()) {
            subtotal += 950;
            nombreTamano = nombreTamano + "  Tenis adidas Super Start color:Rojo  ";
        }
        if (cbxBotas.isSelected()) {
            subtotal += 4500;
            nombreTamano = nombreTamano + "  Botas timberland color:Caf√©  ";
        }

        ivaSub = subtotal * IVA;
        DecimalFormat dec = new DecimalFormat("#.00");
        dec.format(ivaSub);
        total = subtotal + ivaSub;

        txtSubtotal.setText(Double.toString(subtotal));
        txtIVA.setText(Double.toString(ivaSub));
        txtTotal.setText(Double.toString(total));
    }//GEN-LAST:event_btnSumaActionPerformed

